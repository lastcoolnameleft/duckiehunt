<span class="notice">#1 Circulation</span><br/>
The purpose of Duckiehunt is to track the natural and geographic circulation of rubber duckies gifted to me during "The Duckening".
This means that any duck you see in the wild is welcome to be entered into Duckiehunt.com and then passed onto friends and family.
After you enter any duck, you are invited to share with your friends and family to have them fly further.
